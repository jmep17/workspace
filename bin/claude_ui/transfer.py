GH_URL_RE = re.compile(
    r"https?://github\.com/([\w.-]+)/([\w.-]+?)(?:\.git)?(?:/tree/([\w./-]+))?/?$")

def _fetch_github_zip(url):
    m = GH_URL_RE.match((url or "").strip())
    if not m:
        raise ValueError("expected a github.com/owner/repo[/tree/branch[/subdir]] URL")
    owner, repo, tail = m.groups()
    ref, sub = "HEAD", ""
    if tail:
        parts = tail.split("/")
        ref, sub = parts[0], "/".join(parts[1:])
    zurl = f"https://codeload.github.com/{owner}/{repo}/zip/{ref}"
    req = urllib.request.Request(zurl, headers={"User-Agent": "claude-ui"})
    try:
        with urllib.request.urlopen(req, timeout=60) as resp:
            data = resp.read(30 * 1024 * 1024 + 1)
    except (urllib.error.URLError, OSError) as e:
        raise ValueError(f"download failed: {getattr(e, 'reason', e)}") from None
    if len(data) > 30 * 1024 * 1024:
        raise ValueError("download too large (>30 MB)")
    try:
        zf = zipfile.ZipFile(io.BytesIO(data))
    except zipfile.BadZipFile:
        raise ValueError("not a zip — does that branch exist?") from None
    staged = []
    for info in zf.infolist():
        if info.is_dir():
            continue
        parts = Path(info.filename).parts[1:]  # strip <repo>-<ref>/ root
        if not parts:
            continue
        rel = Path(*parts)
        if sub:
            try:
                rel = rel.relative_to(sub)
            except ValueError:
                continue
        if any(x.startswith(".") or x == "node_modules" for x in rel.parts):
            continue
        blob = zf.read(info)
        if len(blob) > 5 * 1024 * 1024:
            continue
        staged.append((rel, blob))
        if len(staged) > 2000:
            raise ValueError("too many files at that URL — point at a subdir")
    if not staged:
        raise ValueError("nothing importable found at that URL")
    return staged

def import_from_url(url, name, type_):
    if type_ not in TYPES:
        raise ValueError("unknown type")
    if not name:
        raise ValueError("name required")
    staged = _fetch_github_zip(url)
    coll = normalize_collection_upload(staged)
    if coll is not None:
        if not NAME_RE.match(name) or name in NON_COLLECTIONS:
            raise ValueError("bad collection name")
        dest = REPO / name
        if dest.exists() or dest.is_symlink():
            raise ValueError(f"{name}: already exists at the repo root")
        write_staged(coll, dest)
        reconcile_links()
        return {"kind": "collection", "path": name, "files": len(coll)}
    if TYPES[type_]["kind"] == "dir":
        link = resolve_skill("active", name)
        path = skill_creation_path(name)
        if link.exists() or link.is_symlink() or path.exists():
            raise ValueError(f"{name}: already exists")
        staged = normalize_skill_upload(staged)
        kind, nskills = classify_skill_upload(staged)
        write_staged(staged, path)
        reconcile_links()
        return {"kind": kind, "path": str(path.relative_to(REPO)),
                "files": len(staged), "skills": nskills}
    ensure_md_collection(type_, name)
    base = TYPES[type_]["root"] / md_rel(name)
    if not any(p.suffix == ".md" for p, _ in staged):
        raise ValueError(f"no .md files found — {type_} are markdown files")
    for rel, _ in staged:
        if (base / rel).exists():
            raise ValueError(f"{rel}: already exists")
    base.mkdir(parents=True, exist_ok=True)
    write_staged(staged, base)
    return {"kind": type_, "path": str(base.relative_to(REPO)), "files": len(staged)}

BOOTSTRAP_SH = """#!/bin/sh
# Generated by claude-ui. Sets up this machine's Claude Code config from the
# repo: symlinks skills/commands/agents/config files into the config dir
# (backing up anything real) and applies the repo's MCP servers.
#
# Usage: ./bootstrap.sh          (after cloning the repo on a new machine)
set -e
REPO="$(cd "$(dirname "$0")" && pwd)"
command -v python3 >/dev/null 2>&1 || { echo "bootstrap: python3 required" >&2; exit 1; }
exec python3 "$REPO/bin/claude-ui" --link-all
"""

def write_bootstrap():
    p = REPO / "bootstrap.sh"
    p.write_text(BOOTSTRAP_SH)
    p.chmod(0o755)
    return str(p.relative_to(REPO))

def link_all():
    """Headless link-everything pass (bootstrap / new machine setup)."""
    for mid in MAPPINGS:
        try:
            r = do_link(mid)
            extra = (" (adopted existing content into the repo — commit it)"
                     if r["adopted"] else
                     f" (previous content saved at {r['backup']})" if r["backup"] else "")
            print(f"  linked  {mid}{extra}")
        except (ValueError, OSError) as e:
            print(f"  skipped {mid}: {e}")
    applied = 0
    try:
        for row in mcp_state()["servers"]:
            if row["source"] and row["status"] in ("repo-only", "differs"):
                mcp_machine_set(row["name"], row["config"])
                applied += 1
        print(f"  mcp     {applied} server(s) applied to ~/.claude.json")
    except (ValueError, OSError) as e:
        print(f"  mcp     skipped: {e}")
    print(f"claude-ui: {config_dir()} now points at {REPO}")

def export_zip(type_=None, scope="active", name=None, collection=None):
    if collection:
        if collection not in collections():
            raise ValueError(f"{collection}: no such collection")
        root, base = REPO / collection, collection
    else:
        if type_ not in TYPES:
            raise ValueError("unknown type")
        if scope not in ("active", "archived"):
            raise ValueError("bad scope")
        root = item_root(type_, scope, name)
        base = Path(name or "item").name
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as z:
        if root.is_file():
            z.write(root, root.name)
        elif root.is_dir():
            for p in sorted(root.rglob("*")):
                rel = p.relative_to(root)
                if p.is_file() and not any(x.startswith(".") for x in rel.parts):
                    z.write(p, str(Path(base) / rel))
        else:
            raise ValueError(f"{name or collection}: not found")
    return buf.getvalue(), base + ".zip"
